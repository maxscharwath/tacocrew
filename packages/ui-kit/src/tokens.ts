/**
 * Design tokens for TacoCrew UI Kit
 *
 * All design tokens are defined as CSS custom properties in:
 * - apps/web/src/theme.css
 *
 * Tailwind CSS v4 automatically generates utility classes from these CSS variables.
 *
 * ## Available Semantic Color Utilities:
 *
 * ### Backgrounds & Text:
 * - `bg-primary` / `text-primary-foreground` - Main brand actions
 * - `bg-destructive` / `text-destructive-foreground` - Error/delete actions
 * - `bg-secondary` / `text-secondary-foreground` - Secondary actions
 * - `bg-accent` / `text-accent-foreground` - Hover states
 * - `bg-muted` / `text-muted-foreground` - Subtle backgrounds
 * - `bg-card` / `text-card-foreground` - Card backgrounds
 * - `bg-input` / `text-input-foreground` - Input fields
 * - `bg-background` / `text-foreground` - Page backgrounds
 *
 * ### Borders & Rings:
 * - `border-border` - Default border color (solid, no transparency)
 * - `ring-ring` - Focus ring color
 *
 * ## Color Palette:
 * All standard Tailwind colors are available:
 * - Brand colors: `brand-{50-950}`
 * - Error colors: `error-{50-950}`
 * - Success colors: `success-{50-950}`
 * - Warning colors: `warning-{50-950}`
 * - Gray colors: `gray-{50-950}`
 * - Plus: rose, amber, emerald, violet, sky, cyan
 *
 * ## Common Patterns:
 *
 * ```tsx
 * // Primary button
 * <button className="bg-primary text-primary-foreground">Submit</button>
 *
 * // Card
 * <div className="bg-card text-card-foreground border border-border rounded-2xl">
 *   Card content
 * </div>
 *
 * // Input with focus ring
 * <input className="bg-input text-input-foreground border-border focus:ring-2 focus:ring-ring" />
 *
 * // Transitions
 * <div className="transition-colors hover:bg-accent">Hover me</div>
 * ```
 */

